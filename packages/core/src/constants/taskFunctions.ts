/**
 * Check if a function address corresponds to a known task function.
 * Automatically masks off the ARM Thumb bit (LSB) before lookup.
 */
export function getTaskFunctionName(address: number): string | null {
  // Mask off bit 0 (Thumb indicator) for lookup
  const lookupAddress = address & ~1;
  return TASK_FUNCTIONS.get(lookupAddress) ?? null;
}